"use client";

import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { Download, RotateCcw, Share2, CheckCircle } from "lucide-react";
import { GenerationResult, GenerationStatus } from "./buzz-cut-simulator";

interface ResultDisplayProps {
  status: GenerationStatus;
  result: GenerationResult | null;
  error: string | null;
  onReset: () => void;
}

export function ResultDisplay({
  status,
  result,
  error,
  onReset,
}: ResultDisplayProps) {
  const handleDownload = async () => {
    if (!result) return;
    
    try {
      const response = await fetch(result.resultImageUrl);
      const blob = await response.blob();
      const url = window.URL.createObjectURL(blob);
      
      const a = document.createElement("a");
      a.href = url;
      a.download = `buzz-cut-${result.id}.png`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      window.URL.revokeObjectURL(url);
    } catch (err) {
      console.error("Failed to download image:", err);
    }
  };

  const handleShare = async () => {
    if (!result) return;
    
    if (navigator.share) {
      try {
        await navigator.share({
          title: "My Buzz Cut Preview",
          text: "Check out my buzz cut preview generated by BuzzCut AI!",
          url: window.location.href,
        });
      } catch (err) {
        console.error("Failed to share:", err);
      }
    } else {
      // Fallback: copy to clipboard
      navigator.clipboard.writeText(window.location.href);
      alert("Link copied to clipboard!");
    }
  };

  if (status === "idle") {
    return (
      <Card className="h-full">
        <CardContent className="p-6 flex items-center justify-center h-64">
          <div className="text-center text-muted-foreground">
            <div className="w-16 h-16 bg-muted rounded-full flex items-center justify-center mx-auto mb-4">
              <CheckCircle className="w-8 h-8" />
            </div>
            <p>Upload an image to get started</p>
          </div>
        </CardContent>
      </Card>
    );
  }

  if (status === "generating") {
    return (
      <Card className="h-full">
        <CardContent className="p-6 flex items-center justify-center h-64">
          <div className="text-center">
            <div className="animate-spin rounded-full h-16 w-16 border-b-2 border-primary mx-auto mb-4"></div>
            <p className="text-lg font-medium">Generating your buzz cut...</p>
            <p className="text-sm text-muted-foreground mt-2">
              Using GPT-4o Vision AI with face preservation
            </p>
            <p className="text-xs text-muted-foreground mt-1">
              This may take up to 60 seconds
            </p>
          </div>
        </CardContent>
      </Card>
    );
  }

  if (status === "error" || error) {
    return (
      <Card className="h-full">
        <CardContent className="p-6 flex items-center justify-center h-64">
          <div className="text-center">
            <div className="w-16 h-16 bg-red-100 rounded-full flex items-center justify-center mx-auto mb-4">
              <RotateCcw className="w-8 h-8 text-red-500" />
            </div>
            <p className="text-lg font-medium text-red-600">Generation Failed</p>
            <p className="text-sm text-muted-foreground mt-2">
              {error || "Something went wrong. Please try again."}
            </p>
            <Button
              onClick={onReset}
              variant="outline"
              className="mt-4"
            >
              Try Again
            </Button>
          </div>
        </CardContent>
      </Card>
    );
  }

  if (status === "success" && result) {
    return (
      <Card className="h-full">
        <CardHeader>
          <CardTitle className="text-lg flex items-center gap-2">
            <CheckCircle className="w-5 h-5 text-green-500" />
            Generation Complete
          </CardTitle>
        </CardHeader>
        <CardContent className="space-y-4">
          {/* Image Comparison */}
          <div className="space-y-6">
            <div>
              <p className="text-sm font-medium mb-3 text-center">Original</p>
              <div className="w-full max-w-xs mx-auto">
                <img
                  src={result.originalImageUrl}
                  alt="Original"
                  className="w-full max-h-48 sm:max-h-56 object-contain rounded-lg bg-muted/20 shadow-sm"
                />
              </div>
            </div>
            <div className="flex justify-center">
              <div className="w-8 h-8 bg-primary/10 rounded-full flex items-center justify-center">
                <span className="text-primary text-lg">â†“</span>
              </div>
            </div>
            <div>
              <p className="text-sm font-medium mb-3 text-center">Buzz Cut</p>
              <div className="w-full max-w-xs mx-auto">
                <img
                  src={result.resultImageUrl}
                  alt="Buzz Cut Result"
                  className="w-full max-h-48 sm:max-h-56 object-contain rounded-lg bg-muted/20 shadow-sm"
                />
              </div>
            </div>
          </div>
          
          {/* Action Buttons */}
          <div className="flex gap-2">
            <Button
              onClick={handleDownload}
              className="flex-1"
              size="sm"
            >
              <Download className="w-4 h-4 mr-2" />
              Download
            </Button>
            <Button
              onClick={handleShare}
              variant="outline"
              size="sm"
            >
              <Share2 className="w-4 h-4" />
            </Button>
            <Button
              onClick={onReset}
              variant="outline"
              size="sm"
            >
              <RotateCcw className="w-4 h-4" />
            </Button>
          </div>
          
          {/* Watermark Notice for Free Users */}
          <div className="p-3 bg-yellow-50 border border-yellow-200 rounded-lg">
            <p className="text-sm text-yellow-800">
              Free version includes watermark. Upgrade to Pro for 4K downloads without watermark.
            </p>
          </div>
        </CardContent>
      </Card>
    );
  }

  return null;
}